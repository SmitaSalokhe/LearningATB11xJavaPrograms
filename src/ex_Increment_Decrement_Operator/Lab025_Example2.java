package ex_Increment_Decrement_Operator;

public class Lab025_Example2
{
    public static void main(String[] args) {
        int a=10;
        System.out.println(++a + ++a); //23
        System.out.println(a);         //12
    }
}
 // a = 10
// (1st ++a value )   ++a= 10+1= 11
// (2nd ++a value )   ++a= 11 +1 = 12
